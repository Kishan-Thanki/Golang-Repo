package main

import (
	"fmt"
)

func main() {

	// Example 1: Integer Addition
	fmt.Println(1 + 1) // Prints the result of 1 + 1, which is 2.

	// Example 2: String Output
	fmt.Println("Hello World") // Prints the string "Hello World" to the console.

	// Example 3: Boolean Output
	fmt.Println(true) // Prints the boolean value 'true' directly to the console.

	// Example 4: Float Output
	fmt.Println(10.5) // Prints the float value 10.5 to the console.

	// Example 5: Division of Float Numbers
	fmt.Println(7.0 / 3.0) // Performs division of two float numbers and prints the result (2.333...).
}
